# Operacoes condicionais if, elif e else

entrada =  'entrar'
condicao = False
condicao1 = False
condicao2 = False
condicao3 = False


if entrada == 'entrar':
    print('Voce decidiu entrar')
elif entrada == 'sair':
    print('Voce decidiu sair')
else:
    print('Digite uma opcao valida')


if condicao:
    print('1')
elif condicao1:
    print('2')
elif condicao2:
    print('3')
elif condicao3:
    print('4')
else:
    print('0')